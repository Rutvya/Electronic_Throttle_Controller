#include"PWM.h"

/******************************************************************************
*                      Public functions definitions
******************************************************************************/

/******************************************************************************
* Name: PWM_init ()
* Description:  Initialize PWM module for OC0A pin

* Arguments: None
* Returns: None
******************************************************************************/
void PWM_init()
{
    TCCR0A|=((1<<WGM01)|(1<<WGM00));
    TCNT0=0x00;
    TCCR0B |= ((1 << CS00) | (1 << CS02));
    TCCR0B &= ~(1 << CS01);
}

/******************************************************************************
* Name: PWM_disable ()
* Description:  Disable PWM module for OC0A pin

* Arguments: None
* Returns: None
******************************************************************************/
void PWM_disable()
{
    TCCR0A&=~(1<<COM0A1);
    TCCR0A&=~(1<<COM0A0);
}

/******************************************************************************
* Name: PWM_enable ()
* Description:  Enable PWM module for OC0A pin

* Arguments: None
* Returns: None
******************************************************************************/
void PWM_enable()
{
    TCCR0A|=(1<<COM0A1);
    TCCR0A&=~(1<<COM0A0);
}

